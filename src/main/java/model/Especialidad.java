package model;


public class Especialidad {
    private int IDESP;
    private String NOMESP;
    private String ESTDESP;
    private String TIPOESP;
    private String ENCARGADO;
    private int IDTRA;

    public int getIDESP() {
        return IDESP;
    }

    public void setIDESP(int IDESP) {
        this.IDESP = IDESP;
    }

    public String getNOMESP() {
        return NOMESP;
    }

    public void setNOMESP(String NOMESP) {
        this.NOMESP = NOMESP;
    }

    public String getESTDESP() {
        return ESTDESP;
    }

    public void setESTDESP(String ESTDESP) {
        this.ESTDESP = ESTDESP;
    }

    public String getENCARGADO() {
        return ENCARGADO;
    }

    public void setENCARGADO(String ENCARGADO) {
        this.ENCARGADO = ENCARGADO;
    }

    public int getIDTRA() {
        return IDTRA;
    }

    public void setIDTRA(int IDTRA) {
        this.IDTRA = IDTRA;
    }

    public String getTIPOESP() {
        return TIPOESP;
    }

    public void setTIPOESP(String TIPOESP) {
        this.TIPOESP = TIPOESP;
    }
   
}
